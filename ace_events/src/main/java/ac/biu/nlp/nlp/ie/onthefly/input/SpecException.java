package ac.biu.nlp.nlp.ie.onthefly.input;

public class SpecException extends Exception {

	private static final long serialVersionUID = 2673154851693718543L;

	public SpecException(String message) {
		super(message);
	}

	public SpecException(Throwable cause) {
		super(cause);
	}

	public SpecException(String message, Throwable cause) {
		super(message, cause);
	}

}
